function mostrar()
{
	let mesDelAño;
	mesDelAño = document.getElementById("txtIdMes").value;
	switch (mesDelAño) {
			case "Febrero":
			alert(" 29 días.");
			break;
			case "Marzo":
				case "Enero":
					case "Mayo":
						case "Julio":
							case "Agosto":
								case "Octubre":
									case "Diciembre":
				alert("31 días.");
			break;
			default:
				alert("30 días.");
			break;
}
}//FIN DE LA FUNCIÓN